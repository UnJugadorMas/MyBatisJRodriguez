public class Nationalities {
    private int id;
    private String name;

    public Nationalities(int iD, String name) {
        super();
        id = iD;
        this.name = name;
    }

    public int getID() {
        return id;
    }
    public void setID(int iD) {
        id = iD;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
